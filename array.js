const arr = [1, 2, 3, "Nicaisse"]

// Add element at the end of array
arr.push(4)
// Remove element from the end of array
arr.pop()


// Add element at the begining of array
arr.unshift(0)
// Remove element at the begining of array
arr.shift() 


// Iterate over elements of array
for(const item of arr) {
    console.log(item)
}


